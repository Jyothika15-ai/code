a=int(input("Enter the mark :"))
if(a>=90 & a<=100):
    print("Grade A+")
elif(a>=80 & a<=90):
    print("Grade A")
elif(a>=70 & a<=80):
    print("Grade B")
elif(a>=60 & a<=70):
    print("Grade C")
elif(a>=50 & a<=60):
    print("Grade D")
elif(a>=0 & a<=0):
    print("Failed")
else:
    print("Invadid")